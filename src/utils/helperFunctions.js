export function calculateAveragePerSubject(prelim, midterm, final, unit) {
  return (
    (parseFloat(prelim) + parseFloat(midterm) + parseFloat(final)) /
    parseFloat(unit)
  ).toFixed(2);
}

export function calculateGWA(subjects, period) {
  let totalUnits = 0;

  let totalFinalGrade = 0;

  subjects.forEach((item) => {
    if (item.subject && item.subject.course_unit) {
      totalUnits += item.subject.course_unit;
    }
  });

  subjects.forEach((item) => {
    if (item.subject && item.finalGrade) {
      totalFinalGrade += item.finalGrade * item.subject.course_unit;
    }
  });

  if (period === 'finalGrade') {
    return (totalFinalGrade / totalUnits).toFixed(2);
  }
}

export function calculateTotalGrade(subject) {
  const periods = [
    'first_period',
    'second_period',
    'third_period',
    'fourth_period',
  ];
  let total = 0;
  let count = 0;

  for (const period of periods) {
    const value = parseFloat(subject[period]);
    if (!isNaN(value) && value > 0) {
      total += value;
      count++;
    }
  }

  if (count === 0) {
    return null; // No valid and non-zero period values
  }

  const average = total / count;
  const passed = average >= 75;

  return { total: Math.round(average), passed };
}

